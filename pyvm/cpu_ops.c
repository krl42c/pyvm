#include <stdint.h>

int32_t i_add(int32_t lval, int32_t rval) { return lval + rval; }
int32_t i_sub(int32_t lval, int32_t rval) { return lval - rval; }
int32_t i_mul(int32_t lval, int32_t rval) { return lval * rval; }
int32_t i_div(int32_t lval, int32_t rval) { return lval / rval; }

float f_add(float lval, float rval) { return lval + rval; }
float f_sub(float lval, float rval) { return lval - rval; }
float f_mul(float lval, float rval) { return lval * rval; }
float f_div(float lval, float rval) { return lval / rval; }
